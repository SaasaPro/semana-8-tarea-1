/* Variables 
    let, const, var
    VAR --> ES5 (Ya no se usa)
    LET --> ES6 Se usa para definir una variable que cambiara de valor. Es posible declarar una variable sin definir su valor.
    CONST --> ES6 Se usa para definir una variable que no cambiara de valor

    Tipos de datos vistos hasta ahora:
    String: Cadenas de caracteres (Comillas dobles o simples)
    Numerico: Enteros y decimales
    Boolean: Admite valores logicos (true o false)
    
*/

